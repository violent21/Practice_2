@WebServlet(name = "helloServlet", value = "/hello-servlet")
public class HelloServlet extends HttpServlet {
    private String message;

    public void init() {
        message = "Hello World!";
    }

    public void doGet(HttpServletRequest request, HttpServletResponse response) throws IOException {
        response.setContentType("text/html");

        String name = request.getParameter("name"); // получаем значение параметра name из запроса

        PrintWriter out = response.getWriter();
        out.println("<html><body>");
        out.println("<h1>" + (name != null ? "Hello, " + name + "!" : message) + "</h1>"); // если name не null, то выводим приветствие с именем, иначе - обычное приветствие
        out.println("<form method='POST' enctype='multipart/form-data'>"); // создаём форму для загрузки файла
        out.println("<input type='file' name='file'/>");
        out.println("<br/>");
        out.println("<input type='submit' value='Upload'/>");
        out.println("</form>");
        out.println("</body></html>");
    }

    public void doPost(HttpServletRequest request, HttpServletResponse response) throws IOException, ServletException {
        response.setContentType("text/html");

        // получаем файл из запроса
        Part filePart = request.getPart("file");
        String fileName = filePart.getSubmittedFileName();
        InputStream fileContent = filePart.getInputStream();

        // сохраняем файл на диск
        OutputStream outputStream = new FileOutputStream(new File("/Users/mac/Desktop/input.rtf", fileName));
        byte[] buffer = new byte[1024];
        int bytesRead;
        while ((bytesRead = fileContent.read(buffer)) != -1) {
            outputStream.write(buffer, 0, bytesRead);
        }
        outputStream.close();
        fileContent.close();

        // отображаем список загруженных файлов
        File uploadedFilesDir = new File("/path/to/uploaded/files");
        File[] uploadedFiles = uploadedFilesDir.listFiles();
        PrintWriter out = response.getWriter();
        out.println("<html><body>");
        out.println("<h1>Uploaded Files:</h1>");
        if (uploadedFiles != null) {
            for (File file : uploadedFiles) {
                out.println("<p>" + file.getName() + "</p>");
            }
        }
        out.println("</body></html>");
    }


    public void destroy() {
    }
}

